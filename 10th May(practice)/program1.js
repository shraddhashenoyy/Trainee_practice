//Conditional Statements
//if-statement and if-else Statements
//Example-1
let marks = 40;
if(marks>=45){
    console.log("Passed the Examination");
}else{
    console.log("Failed!!!");
}

//Example-2
let number=25;
if(number>0){
    console.log("The number is positive");
}
else{
    console.log("Negative number");
}

//Example-3
let age=17;
if(age>=18){
    console.log("Adult");
}else{
    console.log("Minor");
}


